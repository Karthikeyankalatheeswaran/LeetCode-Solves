class Solution(object):
    def romanToInt(self, s):
        """
        :type s: str
        :rtype: int
        """
        d= {'I': 1, 'V' : 5, 'X' : 10, 'L' : 50, 'C': 100, 'D': 500, 'M':1000}

        sums = 0
        n = len(s)
        i = 0 

        while i < n:
            if i < n-1 and d[s[i]] < d[s[i+1]]:
                sums+= d[s[i+1]] - d[s[i]]
                i +=2
            else : 
                sums += d[s[i]]
                i+=1
        
        return sums